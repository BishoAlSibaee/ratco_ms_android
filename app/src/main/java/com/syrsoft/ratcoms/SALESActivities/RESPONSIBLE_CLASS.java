package com.syrsoft.ratcoms.SALESActivities;

public class RESPONSIBLE_CLASS {

    public int id ;
    public int ClientID ;
    public String Name ;
    public String JobTitle ;
    public String MobileNumber ;
    public String Email ;
    public String Link ;

    public RESPONSIBLE_CLASS(int id, int clientID, String name, String jobTitle, String mobileNumber, String email , String Link) {
        this.id = id;
        ClientID = clientID;
        Name = name;
        JobTitle = jobTitle;
        MobileNumber = mobileNumber;
        Email = email;
        this.Link = Link ;
    }
}
