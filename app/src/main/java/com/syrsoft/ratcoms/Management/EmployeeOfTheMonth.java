package com.syrsoft.ratcoms.Management;

public class EmployeeOfTheMonth {

    public int id ;
    public int JobNumber ;
    public String Name ;
    public String Year ;
    public String Month ;
    public int Rank ;
    public int Rating ;

    public EmployeeOfTheMonth(int id, int jobNumber, String name, String year, String month, int rank, int rating) {
        this.id = id;
        JobNumber = jobNumber;
        Name = name;
        Year = year;
        Month = month;
        Rank = rank;
        Rating = rating;
    }
}
